    private boolean deleteStudent(String registrationNumber) throws SQLException {
        PreparedStatement statement = null;

        try {
            // Prepare SQL statement (using parameterized query for security)
            String sql = "DELETE FROM students WHERE Registration_No = ?";
            statement = con.prepareStatement(sql);
            statement.setString(1, registrationNumber);

            // Execute the delete query and return true if successful (rows affected > 0)
            int rowsAffected = statement.executeUpdate();
            if (rowsAffected > 0) {
                return true;
            } else {
                return false;
            }
        } catch (SQLException e) {
            e.printStackTrace();
            return false; // Indicate deletion failure
        } finally {
            // Close resources
            if (statement != null) {
                statement.close();
            }
        }
    }
