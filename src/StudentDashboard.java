    private void rSButtonHover12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonHover12ActionPerformed
        // TODO add your handling code here:
        if (registra != null && !registra.trim().isEmpty()) {
    try {
        int response = JOptionPane.showConfirmDialog(this, "Do you agree you want to delete your account?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if (response == JOptionPane.YES_OPTION) {
            boolean isDeleted = deleteStudent(registra);
            if (isDeleted) {
                JOptionPane.showMessageDialog(this, "Student data deleted successfully.");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete student data.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Deletion cancelled.");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "An error occurred while deleting student data.");
    }
} else {
    JOptionPane.showMessageDialog(this, "Registration number cannot be empty.");
}

    }//GEN-LAST:event_rSButtonHover12ActionPerformed

