    private void rSButtonHover9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonHover9ActionPerformed
        // TODO add your handling code here:
        String searchTerm = searchfield.getText().trim();
        if (!searchTerm.isEmpty()) {
            // Clear the table selection
            jTable1.clearSelection();

            // Loop through each row in the table and search for the searchTerm
            for (int row = 0; row < jTable1.getRowCount(); row++) {
                String rollNo = jTable1.getValueAt(row, 0).toString();
                if (rollNo.equalsIgnoreCase(searchTerm)) {
                    // Select the row if the roll number matches the searchTerm
                    jTable1.setRowSelectionInterval(row, row);
                    jTable1.scrollRectToVisible(jTable1.getCellRect(row, 0, true));
                    return; // Stop searching after finding the first match
                }
            }
            // If no match is found, display a message
            JOptionPane.showMessageDialog(null, "Roll number not found.");
        } else {
            // If the search field is empty, display a message
            JOptionPane.showMessageDialog(null, "Please enter a roll number to search.");
        }
    }//GEN-LAST:event_rSButtonHover9ActionPerformed

