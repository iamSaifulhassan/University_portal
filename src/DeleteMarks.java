    private void rSButtonHover8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonHover8ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            // Get the Registration_No of the selected row
            String registrationNo = jTable1.getValueAt(selectedRow, 0).toString();

            // Execute SQL DELETE query to remove the row from the database
            try {
                String sql = "DELETE FROM scores WHERE Registration_No = ?";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, registrationNo);

                int rowsAffected = ps.executeUpdate();
                if (rowsAffected > 0) {
                    // Remove the row from the table
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.removeRow(selectedRow);
                    JOptionPane.showMessageDialog(null, "Row deleted successfully.");
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to delete row.");
                }
                ps.close();
            } catch (SQLException ex) {
                Logger.getLogger(DeleteMarks.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row to delete.");
        }
    }//GEN-LAST:event_rSButtonHover8ActionPerformed

